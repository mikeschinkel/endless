package verify

import (
	"strings"
)

// RenderRunScript renders a POSIX sh script that runs a task's verification with
// no Endless present, by exit code alone. It is the bare-clone emission of the
// E-1596 contract (section D): a no-Endless clone runs every check, and the
// script exits 0 only when all pass.
//
// The script runs the merged Setup steps, then each check's ResolvedCommand in
// order. Teardown steps run via a trap so they execute even when a check fails.
// Seed and Needs are deliberately excluded: seeding and substrate provisioning
// are Endless-only enhancements unavailable in bare mode.
//
// RenderRunScript assumes m has passed validation (so every ResolvedCommand is
// concrete); it does not validate.
func RenderRunScript(m *Manifest) (script string) {
	var b strings.Builder

	b.WriteString("#!/usr/bin/env sh\n")
	b.WriteString("# Generated by Endless from .endless/tasks/")
	b.WriteString(m.Task)
	b.WriteString("/verify.toml — DO NOT EDIT.\n")
	b.WriteString("# Runs this task's verification with no Endless present.\n")
	b.WriteString("# Exits 0 only if every check passes; non-zero on the first failure.\n")
	b.WriteString("set -eu\n\n")

	// Teardown runs on any exit (success, failure, or interrupt) so cleanup is
	// not skipped when a check fails under set -e.
	if len(m.Teardown) > 0 {
		b.WriteString("teardown() {\n")
		for _, step := range m.Teardown {
			b.WriteString("\t")
			b.WriteString(step)
			b.WriteString("\n")
		}
		b.WriteString("}\n")
		b.WriteString("trap teardown EXIT\n\n")
	}

	if len(m.Setup) > 0 {
		b.WriteString("# setup\n")
		for _, step := range m.Setup {
			b.WriteString(step)
			b.WriteString("\n")
		}
		b.WriteString("\n")
	}

	b.WriteString("# checks\n")
	for _, c := range m.Checks {
		b.WriteString(c.ResolvedCommand())
		b.WriteString("\n")
	}

	script = b.String()
	return script
}
